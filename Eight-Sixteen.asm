; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; The program prints strings in an 8x16 font by
; taking the ascii value of each character 
; and comparing it to its FONT_DATA and printing
; the string Row by row. When a bit in the FONT_DATA
; is a 1 the program prints a '*' and when
; the bit is a 0 the program prints a ' '

.ORIG x3000

;------------------------------------------------------------------------------------------------
;RESETTING REGISTERS TO 0
;-------------------------
AND R0,R0,#0    ; R0 Used to store output character
AND R1,R1,#0    ; R1 Used to store address of current string character
AND R2,R2,#0    ; R2 Used to store calculated address -> ASCII[Char]*16 + Row Number + FONT_DATA
AND R3,R3,#0    ; R3 used as a counter going from 16 to 0
AND R4,R4,#0    ; R4 used as a counter keeping track of Row Number
AND R5,R5,#0	; R5 is used to store #-1
ADD R5,R5,#1	; R5<-R5+1
NOT R5,R5	; R5<-NOT[R5]
ADD R5,R5,#1	; R5<-R5+1
ADD R4,R4,R5	; R4<-#-1
AND R6,R6,#0    ; R6 is used as a counter from 7 to 0 to print Spaces and Stars
AND R7,R7,#0	; R7 is used to store address of FONT_DATA
;-----------------------------------------------------------------------------------------------
	ADD R3,R3,#15   ; Load R3 with number 15
	ADD R3,R3,#1	; Load R3 with number 16

LOOP 	AND R0,R0,#0	; Reset R0
	LD R0,NLINE 	; Load R0 with Newline character
	OUT 		; Print a newline
	ADD R3,R3,R5    ; REDUCE R3 by 1
	BRz EXIT	; If R3 == 0 Exit the program
	ADD R4,R4,#1	; INCREMEMT ROW TRACKER	
	LEA R1,STAS     ; R1 contains location of STAS
 	LDR R1,R1,#0    ; R1 Contains x5002
	
;------------------------------------------------------------------------------------------------

LOOP2 	LDR R2,R1,#0	; STORE VALUE at x500. in R2
	BRz LOOP	; If Value stored was Null go to LOOP
	ADD R1,R1,#1	; R1 <- R1 + 1
	ADD R2,R2,R2	; Multiply value in R2 by 16
	ADD R2,R2,R2	; 	''
	ADD R2,R2,R2	;	''
	ADD R2,R2,R2	;	''
	ADD R2,R2,R4	 ; ADD Row number to the value
	LEA R7,FONT_DATA ; Store Address of FONT_DATA in R7
	ADD R2,R2,R7	 ; Add FONT_DATA to complete memory address calculation
	AND R6,R6,#0	 ; Set R6 to #0
	ADD R6,R6,#8	 ; Set R6 to #8
	LDR R2,R2,#0	 ; Find value at the calculated address. Put in R2

;------------------------------------------------------------------------------------------------
	 	
LOOP3 	ADD R2,R2,#0	 ; Statement does nothing. Used to make testing easier.
	BRzp PRINTSPACE	 ; If R2 is positive or zero[LEADING BIT IS 0] goto PRINTSPACE
	BRn  PRINTSTAR	 ; If R2 is negative[LEADING BIT IS 1] goto PRINTSTAR
CONT	ADD R2,R2,R2	 ; Multiply R2 by 2 hence discarding the leading bit
	ADD R6,R6,R5	 ; Decrement counter
	BRnp LOOP3	 ; Continue loop if counter not 0
	BRz LOOP2	 ; Exit this loop if counter 0

;------------------------------------------------------------------------------------------------

PRINTSPACE LEA R0,BIT0	 ; Get address of BIT0. Store in R0
	   LDR R0,R0,#0  ; GET address x5000. Store in R0
	   LDR R0,R0,#0  ; Get character stored at x5000. Store in R0
	   OUT		 ; Print character in R0
	   AND R0,R0,#0	 ; CLEAR R0
	   BRnzp CONT	 ; GO TO CONT so as to CONTINUE THE LOOP

;------------------------------------------------------------------------------------------------

PRINTSTAR LEA R0,BIT1	 ; Get address of BIT1. Store in R0
	  LDR R0,R0,#0	 ; Get address x5001. Store in R0
	  LDR R0,R0,#0	 ; Get character stored at x5001. Store in R0
	  OUT		 ; Print character in R0
	  AND R0,R0,#0	 ; CLEAR R0
	  BRnzp CONT	 ; GO TO CONT so as to CONTINUE THE LOOP

;------------------------------------------------------------------------------------------------

EXIT 	  TRAP x25	 ; HALT THE PROGRAM

;------------------------------------------------------------------------------------------------

BIT0    .FILL x5000      ; LOCATION WHERE ' ' is stored
BIT1    .FILL x5001 	 ; LOCATION WHERE '*' is stored
STAS    .FILL x5002	 ; LOCATION WHERE String starts
MINUS1  .FILL xFFFF	 ; Used to represent #-1
NLINE   .FILL x0A  	 ; Used to represent NewLine Character

